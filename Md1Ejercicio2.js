let a = parseInt (prompt('Ingrese primer numero'));
let b = parseInt (prompt('Ingrese Segundo numero'));

function suma(){
    let suma = (a+b);
    console.log ("Resultado de la suma es:" +suma);
}
function resta(){
    let resta = (a-b);
    console.log ("Resultado de la resta es:" +resta);
}
function multiplicacion(){  
    let multiplicacion = (a*b);  
    console.log ("Resultado de la multiplicacion es:" +multiplicacion);
}
function division(){
    let division = (a/b);
    console.log ("Resultado de la division es:" +division);
}
suma();
resta();
multiplicacion();
division();


